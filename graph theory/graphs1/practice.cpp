vector<int> arr[maxN];
bool vis[maxN], onStack[maxN];
int intime[maxN], low[maxN], timer;
stack<int> st;

void dfs(int node)
{
    vis[node] = true;
    onStack[node] = true;
    st.push(node);
    low[node] = intime[node] = ++timer;

    for (int child : arr[node])
    {
        if (vis[child] && onStack[child])
        {
            low[node] = min(low[node], intime[child]);
        }
        else if (!vis[child])
        {
            dfs(child);

            if (onStack[child])
            {
                low[node] = min(low[node], intime[child]);
            }
        }
    }

    if (low[node] == intime[node])
    {
        while (true)
        {
            int u = st.top();
            onStack[u] = false;
            st.pop();

            cout << u << " ";

            if (u == node)
                break;
        }
        cout << endl;
    }
}

